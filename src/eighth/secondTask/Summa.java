package eighth.secondTask;

public class Summa {

    int sum(int a, int b){
        return a + b;
    }

    double sum(int a, int b, int c){
        return a + b + c;
    }

    double sum(double x, double y){
        return x + y;
    }

    double sum(double x, double y, double z){
        return x + y + z;
    }

    double sum(int a, double x, double y){
        return a + x + y;
    }

    double sum(Integer a, Integer b){
        return a + b;
    }
}
